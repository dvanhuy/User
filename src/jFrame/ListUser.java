        {
            defaultTableModel.addRow(new Object[]{i.getId(),i.getTen(),i.getSdt(),i.getTenTK(),i.getMatKhau()
                    ,i.getGioiThieu(),i.getVaiTro(),i.getSoThich()});
        } 
