    private void deleteuserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteuserActionPerformed
        int row = userTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(ListUser.this, "Vui long chon user truoc", "Loi"
                    , JOptionPane.ERROR_MESSAGE);
        }
        else 
        {
            int confirm = JOptionPane.showConfirmDialog(ListUser.this, "Ban chac chan muon xoa khong?");
            if (confirm == JOptionPane. YES_OPTION) 
            {
                int userId = Integer.valueOf(String.valueOf(userTable.getValueAt (row, 0)));
                userService.deteleUser(userId);
            }
        } 
        defaultTableModel.setRowCount(0);
        setTableData(userService.getAllUser());
    }//GEN-LAST:event_deleteuserActionPerformed

